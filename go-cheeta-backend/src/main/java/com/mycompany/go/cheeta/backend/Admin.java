package com.mycompany.go.cheeta.backend;

/**
 *
 * @author HP
 */
public class Admin {
    private String id;
    private String name;
    private String email;
    private String mobile;
    private String password;

    public Admin(String id, String name, String email, String mobile, String password) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.mobile = mobile;
        this.password = password;
    }

    public Admin() {
        this.id = "";
        this.name = "";
        this.email = "";
        this.mobile = "";
        this.password = "";
    }
    
    public Admin(String id) {
        this.id = id;
    }

    public void getAdminDetails(String id) {
        this.id = id;
    }
    
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
    
}
